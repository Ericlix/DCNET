package jp.dcnet.lss.chapter09;

public class Animal_1 {
	private int age;
	private String name;
	private String type;

	public Animal_1(int age,String name,String type){
		this.age = age;
		this.name = name;
		this.type = type;
	}

	public int getAge() {
		return age;
	}
	public String getName() {
		return name;
	}
	public String getType() {
		return type;
	}

}
