public class Test2 {
	//关系演算子
	public static void main(String[] args){
		int a = 10;
		int b = 20;
		//boolean c = a > b;跟以下无关
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a < b);
		System.out.println(a <= b);
	}

}
